 * Использовать kotlin.math.cos и другие стандартные реализации функции косинуса в этой задаче запрещается.
 */
fun cos(x: Double, eps: Double): Double {
    val y = x % (PI * 2)
    var result = 1.0
    var power = 2
    var plusMinus = -1
    var number = y
    while (abs(number) >= eps) {
        number = y.pow(power) / factorial(power)
        result += number * plusMinus
        plusMinus *= -1
        power += 2
    }
    return result
}

/**
 * Сложная (4 балла)
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var number = 1.0
    var digitsInLine = 0
    while (digitsInLine < n) {
        var numberDouble = (number.pow(2)).toInt()
        digitsInLine += digitNumber(numberDouble)
        number += 1
    }
    var e = ((number - 1).pow(2)).toInt()
    return forSequenceDigit(digitsInLine, e, n)
}

/**
 * Сложная (5 баллов)
 *
 * Найти n-ю цифру последовательности из чисел Фибоначчи (см. функцию fib выше):
 * 1123581321345589144...
 * Например, 2-я цифра равна 1, 9-я 2, 14-я 5.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun fibSequenceDigit(n: Int): Int {
    if (n <= 2) return 1
    var fib1 = 1
    var fib2 = 1
    var digitsInLine = 2
    while (digitsInLine < n) {
        val fib = fib1 + fib2
        digitsInLine += digitNumber(fib)
        fib1 = fib2
        fib2 = fib
    }
    return forSequenceDigit(digitsInLine, fib2, n)
}

private fun forSequenceDigit(digitsInLine: Int, e: Int, n: Int): Int {
    val digitsLeft = digitsInLine - n
    var digitsLeft1 = digitsLeft
    var e1 = e
    while (digitsLeft1 > 0) {
        e1 /= 10
        digitsLeft1 -= 1
    }
    return e1 % 10
}

